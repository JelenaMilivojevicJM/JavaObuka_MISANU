package zadatak3_02Feb2023;

public class Djak extends Osoba{

	String nazivSkole;
	String razred;
	
	Djak(String ime,String datumRodjenja,String adresaStanovanja,String nazivSkole,String razred ){
		super(ime,datumRodjenja,adresaStanovanja);
		this.nazivSkole = nazivSkole;
		this.razred = razred;
	}
	
	public String toString() {
	return "Djak: "+super.toString()+"\nNaziv Skole: "+nazivSkole+"\nRazred: "+razred;
	}
}
