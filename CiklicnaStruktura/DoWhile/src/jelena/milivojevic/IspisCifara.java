package jelena.milivojevic;

public class IspisCifara {

	public static void main(String[] args) {
		/*Napisati program kojim se štampaju svih deset cifara, od vece ka manjoj korišćenjem  */
		// Do-While petlja - Iterativni ciklus
				System.out.print("\nDoWhile: ");
				int j = 9;
				do{
					System.out.print(j + " ");
					j--;
				}while (j >= 0);

	}

}
