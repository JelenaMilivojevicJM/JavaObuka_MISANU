package jelena.milivojevic;

public class IspisCifara {

	public static void main(String[] args) {
		/*
		 * Napisati program kojim se štampaju svih deset cifara, od vece ka manjoj
		 * korišćenjem
		 */
		// While petlja - Iterativni ciklus
		System.out.print("\nWhile: ");
		int i = 9;
		while (i >= 0) {
			System.out.print(i + " ");
			i--;
		}

	}

}
