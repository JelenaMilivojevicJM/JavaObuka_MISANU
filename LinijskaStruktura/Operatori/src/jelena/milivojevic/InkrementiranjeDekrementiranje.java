package jelena.milivojevic;

public class InkrementiranjeDekrementiranje {

	public static void main(String[] args) {
		/*Deklarisati promenljivu B i dodeliti joj vrednost 5. Primeniti operator inkrementiranja i ispisati rezultat a onda primeniti operator dekrementiranja i ispisati rezultat. */

		int B = 5;

		B++; // primena operatora inkrementiranja
		System.out.println(B);

		
		B--; // primena operatora dekrementiranja
		System.out.println(B);
	}

}
